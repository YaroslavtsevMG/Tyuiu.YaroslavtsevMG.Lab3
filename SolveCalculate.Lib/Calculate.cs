namespace SolveCalculate.Lib
{
    public static class Calculate
    {
        public static float CalculateSumma(float a, float b)
        {
            return a + b;
        }
        public static float CalculateDifference(float a, float b)
        {
            return a - b;
        }
        public static float CalculateMultiplication(float a, float b)
        {
            return a * b;
        }
        public static float CalculateQuotient(float a, float b)
        {
            return a / b;
        }
    }
}
